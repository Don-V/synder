#include "global.h"
#include "ui.h"
#include "synmap.h"

int main(int argc, char * argv[]){

    // ------------------------------------------------------------------------
    // Prep input 
    // ------------------------------------------------------------------------
    Arguments args = parse_command(argc, argv);
    Synmap * syn = load_synmap(args.synfile);


    // ------------------------------------------------------------------------
    // Do stuff 
    // ------------------------------------------------------------------------
    print_synmap(syn);


    // ------------------------------------------------------------------------
    // Clean up
    // ------------------------------------------------------------------------
    free_synmap(syn);
    close_Arguments(args);

    return(EXIT_SUCCESS);
}
